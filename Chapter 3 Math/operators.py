# operators.py

def main():
    print ("Integers:")

    print ("4 * 3 is ", 4 * 3)

    print ("4 / 3 is ", 4 / 3) 

    print ("3 / 4 is ", 3 / 4)

    print ("4 % 3 is ", 4 % 3)

    print ("3 % 4 is ", 3 % 4)

    print ()

    print ("Floats:")

    print ("4.0 * 3.0 is ", 4.0 * 3.0)

    print ("4.0 / 3.0 is ", 4.0 / 3.0)

    print ("3.0 / 4.0 is ", 3.0 / 4.0)

    print ()
##
    print ("Association:")

    print ("5 + 4 + 3 is ", 5 + 4 + 3)

    print ("5 - 4 - 3 is ", 5 - 4 - 3 )

    print ("5 * 4 * 3 is ", 5 * 4 * 3)

    print ("5 / 4 / 3 is ", 5 / 4 / 3 )

    print ("5 % 4 % 3 is ", 5 % 4 % 3)

    print ("2 ** 3 ** 2 is ", 2 ** 3 ** 2)

    print()
##
    print ("Precedence:")

    print ("5 + 4 * 3 is ", 5 + 4 * 3)

    print ("5 * 4 - 3 is ", 5 * 4 - 3 )

    print ("5 - 3 / 4 is ", 5 - 3 / 4)

    print ("5 + 4 % 3 is ", 5 + 4 % 3 )

    print ("5 % 3 + 4 is ", 5 % 3 + 4)

    print ("3 % 4 + 5 is ", 3 % 4 + 5)

    print ()

    print ("Mixed types:")

    print ("5.0 + 4 + 3 is ", 5.0 + 4 + 3)

    print ("5 * 4 * 3.0 is ", 5 * 4 * 3.0) 

    print ("6 // 4 / 3.0 is ", 6 // 4 / 3.0)

    print ("6.0 // 4 / 3 is ", 6.0 // 4 / 3) 

main()
